#• + plus                   addition.
print(5 + 15)
#• - minus                  substraction
print(50 - 25)
#• / slash                  division
print(50 / 5)
#• * asterisk               multiplication
print(10 / 5)
#• % percent                division remainder
print(10 % 5)
#• < less-than
print(1 < 5)
#• > greater-than
print(10 > 5)
#• <= less-than-equal
print(5 <= 5)
#• >= greater-than-equal
print(7 >= 7)
